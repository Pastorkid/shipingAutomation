interface TranslationContextType {
  language: string;
  t: TranslationKeys;
  changeLanguage: (lang: string) => void;
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined);

interface TranslationProviderProps {
  children: ReactNode;
}

export function TranslationProvider({ children }: TranslationProviderProps) {
  const [language, setLanguage] = useState('en');
  const [t, setT] = useState(translations.en);

  useEffect(() => {
    let unsubscribe: (() => void) | null = null;
    
    const initializeLanguage = async () => {
      try {
        const store = await import('../store/languageTimeZone/languageTimeZoneStore');
        const languageStore = store.default;
        
        // Set initial values immediately
        const currentLanguage = languageStore.getState().language || 'en';
        setLanguage(currentLanguage);
        setT(translations[currentLanguage] || translations.en);
        
        // Subscribe to changes with immediate update
        unsubscribe = languageStore.subscribe((state: any) => {
          const newLanguage = state.language || 'en';
          // Update immediately without delay
          setLanguage(newLanguage);
          setT(translations[newLanguage] || translations.en);
        });
      } catch (error) {
        console.warn('Language store not available, using defaults:', error);
      }
    };
    
    initializeLanguage();
    
    return () => {
      if (unsubscribe) {
        unsubscribe();
      }
    };
  }, []);

  const changeLanguage = (lang: string) => {
    // Update local state IMMEDIATELY for instant UI response
    setLanguage(lang);
    setT(translations[lang] || translations.en);
    
    // Then update the store asynchronously for persistence
    try {
      import('../store/languageTimeZone/languageTimeZoneStore').then((module) => {
        const store = module.default;
        store.getState().setLanguage(lang);
      });
    } catch (error) {
      console.warn('Could not update language store:', error);
    }
  };

  const contextValue = { language, t, changeLanguage };
  
  return React.createElement(
    TranslationContext.Provider,
    { value: contextValue },
    children
  );
}

export function useTranslation() {
  const context = useContext(TranslationContext);
  if (context === undefined) {
    throw new Error('useTranslation must be used within a TranslationProvider');
  }
  return context;
}
